/**
 * 全角字符转半角
 * 全角字符 U+FF01~U+FF5E 转为半角 U+0021~U+007E，全角空格 U+3000 转为半角空格 U+0020
 */
export function toHalfWidth(input: string): string {
  let result = '';
  for (const char of input) {
    const code = char.codePointAt(0)!;
    if (code === 0x3000) {
      result += '\u0020';
    } else if (code >= 0xff01 && code <= 0xff5e) {
      result += String.fromCodePoint(code - 0xfee0);
    } else {
      result += char;
    }
  }
  return result;
}

/**
 * 繁体转简体
 * 基于内置常用字映射表逐字替换，未命中映射表的字符原样保留
 */
export function toSimplified(input: string): string {
  let result = '';
  for (const char of input) {
    result += TRAD_SIMP_MAP[char] ?? char;
  }
  return result;
}

/**
 * 去除空白与特殊符号
 * 仅保留字母、数字、中文（CJK 基本区与扩展 A 区），其余字符一律剔除
 */
export function stripSymbols(input: string): string {
  return input.replace(/[^a-z0-9\u4e00-\u9fff\u3400-\u4dbf]/g, '');
}

/**
 * 用户名归一化
 * 依次执行：全角→半角 → 小写化 → 繁体→简体 → 去除空白与特殊符号
 */
export function normalizeUsername(input: string): string {
  if (!input) return '';
  const halfWidth = toHalfWidth(input);
  const lower = halfWidth.toLowerCase();
  const simplified = toSimplified(lower);
  return stripSymbols(simplified);
}
